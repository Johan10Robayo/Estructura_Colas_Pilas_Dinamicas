    private void apilarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apilarActionPerformed
       String dato= JOptionPane.showInputDialog(null,"Digite el dato en la pila ");
    
        
       JLabel label=new JLabel();
       label.setText(p.apilar(dato).getInfo());
       label.setHorizontalTextPosition(javax.swing.SwingConstants.CENTER);
       label.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Imagenes/nodod.png")));
        panel.add(label);
        panel.updateUI();
        
    }//GEN-LAST:event_apilarActionPerformed

