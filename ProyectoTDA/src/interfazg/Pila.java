    private void desapilarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_desapilarActionPerformed
        panel.removeAll();
        if (!p.vacia()){
           p.desapilar();
          Nodo aux=p.getInicio();
            while(aux!=null){
          JLabel label=new JLabel(aux.getInfo());
         aux=aux.getSiguiente();
        label.setHorizontalTextPosition(javax.swing.SwingConstants.CENTER);
       label.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Imagenes/nodod.png")));
       panel.add(label);
       panel.updateUI();}
          
        } else{
            JOptionPane.showMessageDialog(null,"pila vacia no se puede desapilar");
        }
      
       
       
    }//GEN-LAST:event_desapilarActionPerformed

